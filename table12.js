//Write a program that prints a multiplication table for numbers up to 12. 


 
 for(let i=1;i<=12;i++){
    var multi=1;
     for(let j=1;j<=12;j++){
         multi=j*i;
         
         console.log(j +"  *  "+i+" = "+multi);
     }           
     console.log("Table of: "+i );             
  }   
            
               
        
